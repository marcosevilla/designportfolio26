#!/usr/bin/env node
// Generates site/lib/bio-content.ts from site/content/bio.md.
// Run automatically via predev / prebuild.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "content", "bio.md");
const OUT = path.join(ROOT, "lib", "bio-content.ts");

const LINK_RE = /\[([^\]]+)\]\(([^)]+)\)/g;

function parseParagraph(line) {
  const segments = [];
  let lastIndex = 0;
  for (const m of line.matchAll(LINK_RE)) {
    const before = line.slice(lastIndex, m.index).trim();
    if (before) segments.push({ text: before });
    segments.push({ text: m[1].trim(), href: m[2].trim() });
    lastIndex = m.index + m[0].length;
  }
  const tail = line.slice(lastIndex).trim();
  if (tail) segments.push({ text: tail });
  return segments;
}

function escape(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function serializeSegment(seg) {
  const parts = [`text: "${escape(seg.text)}"`];
  if (seg.href) parts.push(`href: "${escape(seg.href)}"`);
  return `{ ${parts.join(", ")} }`;
}

function main() {
  const raw = fs.readFileSync(SRC, "utf8");
  const { data, content } = matter(raw);

  const name = data.name ?? "";

  const paragraphs = content
    .split(/\n{2,}/)
    .map((p) => p.replace(/\s+/g, " ").trim())
    .filter(Boolean)
    .map(parseParagraph);

  const out = `// AUTO-GENERATED by scripts/build-bio.mjs from content/bio.md.
// Edit the markdown file, not this file.

export type Segment = { text: string; href?: string };
export type Paragraph = Segment[];

export const HERO_NAME = "${escape(name)}";

export const PARAGRAPHS: Paragraph[] = [
${paragraphs.map((p) => `  [\n${p.map((s) => `    ${serializeSegment(s)},`).join("\n")}\n  ],`).join("\n")}
];
`;

  fs.writeFileSync(OUT, out);
  console.log(`[build-bio] wrote ${path.relative(ROOT, OUT)} (${paragraphs.length} paragraphs)`);
}

main();
